    public void zeroRemove()
    {
        if(textArea.getText().equals("0"))
        {
            textArea.setText("");
        }
    }
    public void buttonOperationsColorGained(JButton button)
    {
        button.setBackground(new Color(255, 255, 255));
        button.setForeground(new Color(255, 149, 1));
    }
    public void buttonOperationsColorLost(JButton button)
    {
        
        button.setBackground(new Color(255, 149, 1));
        button.setForeground(new Color(255, 255, 255));
    }
    public void buttonColorGained(JButton button)
    {
        button.setBackground(new Color(217, 217, 217));
        button.setForeground(new Color(0, 0, 0));
    }
    public void buttonColorLost(JButton button)
    {
        
        button.setBackground(new Color(166, 166, 166));
        button.setForeground(new Color(0, 0, 0));
    }
    public void buttonNumbersColorGained(JButton button)
    {
        button.setBackground(new Color(115, 115, 115));
        button.setForeground(new Color(255, 255, 255));
    }
    public void buttonNumbersColorLost(JButton button)
    {
        
        button.setBackground(new Color(51, 51, 51));
        button.setForeground(new Color(255, 255, 255));
    }
