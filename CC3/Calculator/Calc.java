    private void calculationActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_calculationActionPerformed
        try
        {
            
            textArea1.setText("");
            enableFalse();
            String answer;
            secondNumber = Double.parseDouble(textArea.getText());
        switch (operation)
        {
            case "+":
                result = firstNumber + secondNumber;
                roundOff = Math.round(result * 1000.0) / 1000.0;
                answer = ""+roundOff;
                textArea.setText(answer);
                break;
            case "-":
                result = firstNumber - secondNumber;
                roundOff = Math.round(result * 1000.0) / 1000.0;
                answer = ""+roundOff;
                textArea.setText(answer);
                break;
            case "*":
                result = firstNumber * secondNumber;
                roundOff = Math.round(result * 1000.0) / 1000.0;
                answer = ""+roundOff;
                textArea.setText(answer);
                break;
            case "/":
                result = firstNumber / secondNumber;
                roundOff = Math.round(result * 1000.0) / 1000.0;
                answer = ""+roundOff;
                textArea.setText(answer);
                break;
            case "^":
                result = Math.pow(firstNumber, secondNumber);
                roundOff = Math.round(result * 1000.0) / 1000.0;
                answer = ""+roundOff;
                textArea.setText(answer);
                break;
            case "r":
                result = Math.pow(firstNumber, (1/secondNumber));
                roundOff = Math.round(result * 1000.0) / 1000.0;
                answer = ""+roundOff;
                textArea.setText(answer);
                break;
            
        }
        }
        catch(NumberFormatException e)
        {
            System.out.println(e.getMessage());
        }
    }//GEN-LAST:event_calculationActionPerformed

